package queue

// Queue defines the queue interface
type Queue[T any] interface {
	// Push place the given value in the queue head (FIFO).
	Push(value T)
	// Pop removes the item from the front of the queue
	// If false is returned, it either means 1) there were no items on the queue
	// or 2) the queue is closed depending upon the various implementations.
	Pop() (T, bool)
	// IsEmpty returns true when the queue is empty
	IsEmpty() bool
}
